def main_1_5():
    user_input = input("Введите длины проводов через запятую или пробел: \n")

    # Разделяем ввод по запятым или пробелам и удаляем лишние пробелы
    lengths = user_input.replace(',', ' ').split()

    total_length = 0.0  # Переменная для накопления общей длины

    for length in lengths:
        try:
            # Преобразуем введенные данные в число с плавающей запятой
            total_length += float(length)
        except ValueError:
            print(f"'{length}' не является допустимым числом. Пропускаем это значение.")

    # Выводим итоговую длину проводов
    print(f"Общая длина проводов с сечением провода 1,5 мм2: {total_length:.2f} метров")

